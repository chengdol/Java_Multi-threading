package Chapter4_Thread_Executors_4;

// class to hold result
public class Result
{
	private String name;
	private int value;
	

	public String getName()
	{
		return name;
	}

	public int getValue()
	{
		return value;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public void setValue(int value)
	{
		this.value = value;
	}
	
}
