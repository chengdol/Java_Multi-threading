package Chapter6_Parallel_Reactive_Stream_6;


// basic person class
// used to simply complex Person class
public class BasicPerson
{
	private String name;
	private long age;
	
	
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public long getAge()
	{
		return age;
	}
	public void setAge(long age)
	{
		this.age = age;
	}
	
}
